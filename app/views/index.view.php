<div class="container container-info">
  <p style="font-size: 16px;">PANTHEONS - BATTLE OF GODS™ est un jeu de cartes tactique et stratégique pour deux joueurs, dans lequel ceux-ci incarnent les dieux d'un Panthéon mythologique de leur choix. Tous les Panthéons du jeu sont conçus pour pouvoir s'affronter les uns avec les autres.
  <div class="container text-center p-4">
    <img src="/ressources/icons/Séparateur blanc.png"/>
  </div>
  <p>Les joueurs choisissent secrètement les trois Dieux qui participent à la bataille, puis les activent à tour de rôle par le biais de cartes Actions et de Pouvoirs spécifiques. Les cartes Actions -au nombre de douze- sont identiques pour les deux joueurs. Elles représentent les interactions communes des Dieux (se déplacer, attaquer, se soigner ou encore lancer un sort etc.)</p>
  <p>Les Pouvoirs sont des capacités uniques, propres à chaque Dieu et spécifiques à chaque Panthéon.</p>
  <p>C'est grâce à d'astucieuses combinaisons de ces cartes et de ces Pouvoirs, que les joueurs parviennent à remporter la victoire, en éliminant les trois Dieux du Panthéon adverse.</p>
  <div class="text-center p-5">
    <button class="bouton"><a class="link-light link-underline-opacity-0" href="/Pantheons">Découvrir les Pantheons</a></button>
  </div>
